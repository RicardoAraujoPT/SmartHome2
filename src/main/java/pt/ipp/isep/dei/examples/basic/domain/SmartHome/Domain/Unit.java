package pt.ipp.isep.dei.examples.basic.domain.SmartHome.Domain;

/**
 * Represents a unit of measurement.
 * This enum defines the possible units of measurement in the SmartHome domain.
 */
public enum Unit {


    /**
     * Represents the Celsius unit of measurement.
     */
    Celsius,

    /**
     * Represents the Percentage unit of measurement.
     */
    Percentage,

    /**
     * Represents the Watt per hour unit of measurement.
     */
    Watt_m2,

    OnOff,

    /**
     * Represents the Wind Speed measurement.
     */
    Kmh,






}
